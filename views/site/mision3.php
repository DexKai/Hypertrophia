<strong>CUÍDATE DE LOS RESFRÍOS AL SALIR DEL GIMNASIO</strong>
<p>

En período de invierno y a causa del espantoso frío el gimnasio suele estar temperado para poder realizar nuestro entrenamiento, motivándonos para continuar ejercitando en un ambiente con agradable temperatura. Sin embargo, luego de unas horas tenemos bastante calor y de regreso a casa no consideramos el frío que hace afuera, sino que simplemente disfrutamos del aire fresco, el cual puede provocar una gripe.

Luego de realizar tu entrenamiento diario en el gimnasio en época de invierno, es primordial tomar una ducha tibia para quitar la transpiración, si no te duchas antes de ir a casa entonces intenta secarte muy bien y siempre cambiarte de ropa, ya que ésta al quedar húmeda sobre la piel ayuda a que padezcas un resfrío.

Evita enfriamientos y sigue los consejos de profesionales deportistas, quienes indican que hay que abrigarse muy bien al salir del gimnasio, ya que el cambio de temperatura propensa los resfriados normales y las fuertes gripes. Para ello, utiliza una gran chaqueta que cuide de tu tronco y cuello, o bien utiliza una bufanda y un gorro. 
</p>

<strong>CALZADO DEPORTIVO: CLAVE A LA HORA DE ENTRENAR</strong>
<p>
	
En la actualidad, los medios técnicos y la investigación, ha llevado a las marcas deportivas a desarrollar maravillas tecnológicas para proporcionar mayor seguridad Y comodidad.  Al elegir una zapatilla debemos tener en cuenta algunos factores como, por ejemplo, mejor comprar  a última hora del día, cuando los pies están algo hinchados, teniendo la contextura como cuando se practica deporte. También tenemos que tomar en cuenta que no deben quedar ni muy sueltas ni muy apretadas, debe existir una libertad de movimiento en los dedos y ojalá cuando las pruebes, usar el mismo tipo de calcetín con los que realizas deporte.

Es recomendable no usar zapatillas nuevas a la hora de una competición, ya que pueden existir molestias al no estar previamente acostumbrados a la horma de la zapatilla.

Hay que tener en cuenta que hoy en día existe calzado que está diseñado para soportar un número de kilómetros y horas de entrenamiento a partir de los cuales los sistemas de amortiguación se ven afectados. Se deben cambiar con frecuencia para no arriesgarse a una lesión.

Por último, es muy importante airear el calzado, para evitar el “pie de atleta” y tener en cuenta que a veces las zapatillas que dan buen resultado a un deportista, no tienen por qué ser buenas para otros, así que tómate el tiempo necesario para que puedas entrenar con completa comodidad.
</p>

<strong>PRIMERA CAPA: ¡QUE EL SUDOR NO TE JUEGUE UNA MALA PASADA!</strong>
<p>

En principio, esta ropa debe limitar la pérdida de calor corporal y luchar contra los factores agravantes del frío, que son el viento y la humedad.
Muchas veces nos abrigamos demasiado y transpiramos. La ropa mojada se enfría durante todo el entrenamiento y nos enfermamos, perdiendo así, la regularidad en los trabajos físicos.

Hoy te presentamos “la primera capa” permite tener un mejor control de la temperatura corporal de acuerdo a la intensidad el ejercicio y condiciones climáticas, tiene por objeto el mantener la piel seca mediante la expulsión del sudor.
Es importante que se ajuste lo máximo posible al cuerpo, como si se tratara de una segunda piel. La primera capa siempre debe ser ajustada, especialmente en el tronco, donde están los órganos importantes que hay que abrigar. Con ello se consigue atrapar el aire caliente que genera nuestro cuerpo evitando, así, que se desplace al exterior.

Ahora bien, hay que cuidar que no ajuste en exceso y ralentice la circulación de la sangre.
Las hay de dos tipos. De invierno y de media estación. Les aconsejamos utilizar las de media estación, para usar en un ambiente de gimnasio. Las de invierno, son demasiado abrigadas para correr salir a correr y normalmente se utilizan para actividades más estáticas.

Recuerda siempre que pequeños detalles marcan la diferencia para no detenerse en ninguna ocasión y mantener una buena salud. 
</p>

<strong>CALENTADORES DE PIERNAS, MÁS QUE UNA MODA DE LOS 80'S!</strong>
<p>

Que las bajas temperaturas no te quiten la motivación, te presentamos una prenda ideal para utilizar a la hora de entrenar en invierno y que además puedes combinar a gusto con tu look.

¿Recuerdan la película “flashdance”? bueno, un elemento que se hizo muy popular en los 80’s, fueron los calentadores de piernas, los protagonistas los utilizaban durante sus ensayos y clases de danza. Los jóvenes pensaron que los calentadores que usaban sus ídolos podían llevarse también como prendas de calle, esto dio paso a distintos colores y diseños.

Este simple complemento se utilizan básicamente para calentar los músculos antes de hacer ejercicio o en la primera parte del entrenamiento, previniendo lesiones, sobre todo en esta época fría donde la musculatura y cuerpo se ponen rígidos. Una vez que los músculos alcanzaron una temperatura ideal, se retiran.

En los 80’s el material que se utilizaba para los calentadores de pierna era principalmente lana e hilo, actualmente su tela es lycra,  tejido deportivo elástico que generalmente se mezcla con otro material para mayor comodidad y uso. Algunos calentadores pueden tener pinzas de silicona, otros son antibacterianos y permiten la capacidad de respiración a la piel manteniendo el calor,  reducen el roce y pilling. Y lo mejor es que vienen por tallas no solo por género, si no que según el contorno de la pierna. 

¡Anímate a ocuparlos y cuida tus músculos para seguir entrenando en perfectas condiciones este invierno!
</p>














