<strong>SEIS TRUCOS PARA APROVECHAR MEJOR TU TIEMPO EN Hypertrophia
</strong>

<p>



Apaga el teléfono: o déjalo en la casillero. Para condicionar los músculos hay que conseguir que aumenten las conexiones neuronales, y eso significa más concentración. Si te concentras en el ejercicio conseguirás el doble de resultados que si juegas a Angry Birds entre serie y serie. ¿Whatsapp en la bicicleta estática? Si puedes leer y escribir, no estás esforzándote suficiente.
Cuenta hacia atrás: cuenta las repeticiones de cada serie como si fuera el lanzamiento de un cohete. Te dará mucho más ánimo saber cuántas quedan, en lugar de pensar cuántas faltan. ¿Tonto? Sí, pero funciona.
La repetición perfecta: deja de contar una, dos, tres, etc. y mover el peso con fuerza! La palabra clave es despacio. Si buscas ganar fuerza, levanta el peso en una, bájalo en tres, así que contarás “UNA, un, dos tres, DOS, un, dos tres, TRES…”. Si lo que buscas es ganar masa muscular, sube en tres y baja en tres.
No te olvides de las piernas: Es muy común para los hombres concentrarse en los bíceps y pectorales, pero olvidarse de las sentadillas, el peso muerto y las zancadas estimulan la hormona del crecimiento más que ningún otro ejercicio.
Un día de furia: un día cada dos semanas, vuélvete loco y bate tu récord. Mejor si es en un ejercicio compuesto, como sentadillas, press de banca o dominadas. Tiene que ser un día en el que te encuentres bien, aprovecha para levantar más peso y hacer más repeticiones que nunca hasta entonces. Esfuérzate al límite y olvídate de tu programa habitual de entrenamiento. No te lesiones, pide a un monitor que te vigile.
Busca compañía: Varios estudios corroboran que la mejor forma de ganar músculo y perder grasa son las repeticiones forzadas. Es decir, en la última serie, cuando has llegado al fallo, un compañero te ayuda a levantar el peso (pero muy poco, te hace sufrir) y así haces dos o tres repeticiones más que son las que en realidad cuentan.
