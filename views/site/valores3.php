<strong>HIDRATACIÓN ANTES, DURANTE Y DESPUÉS DEL EJERCICIO</strong>
<p>

Cada 1000 kcal consumidas es necesario ingerir 1 litro de líquido, como establece la teoría y el consenso. Es importante también que el líquido consumido se distribuya adecuadamente en el tiempo, siendo importante que se tomen líquidos durante todo el día, así como antes, durante y después del ejercicio. Este comportamiento es necesario afianzarlo mediante unos hábitos que se adquieren conjuntamente con el entrenamiento físico.

Hidratación antes del ejercicioAntes del ejercicio es recomendable tomar entre 400 y 600 ml de agua o de bebida deportiva entre 1 – 2 horas antes de empezar el entrenamiento para comenzar bien hidratado. La hidratación antes del ejercicio cumple los siguientes objetivos:

Ejerce una función termo-reguladora, evitando que aumente la temperatura central del deportista.

Disminuye la percepción del esfuerzo.

Contribuye a rellenar los depósitos de glucógeno cuando se toma una bebida deportiva rica en hidratos de carbono.

Hidratación durante el ejercicio

Durante el ejercicio la estrategia de hidratación tiene como finalidad la ingesta de líquidos a intervalos regulares en el tiempo. La hidratación durante el ejercicio cumple los siguientes objetivos:

Reponer el agua y los electrolitos perdidos mediante el sudor y la orina.

Mantener los niveles de glucosa en sangre estables.

Hidratación después del ejercicio

Las recomendaciones sugieren tomar entre 200 – 250 ml de agua o bebida deportiva cada 20 – 25 minutos de ejercicio físico, lo que equivale a dos o tres sorbos de líquido.

No siempre es posible alcanzar el equilibrio hídrico, ya que la cantidad de líquido perdida mediante el sudor puede ser superior a la capacidad de vaciamiento gástrico. Es decir, en determinadas circunstancias se podría llegar al caso de perder más líquido del que nuestro organismo es capaz de asimilar.

La rehidratación después del ejercicio es tan importante como la hidratación antes y durante el mismo. Este proceso debería comenzar lo antes posible, posibilitando una adecuada recuperación.

Para calcular la pérdida de líquido mediante el sudor, nos podemos pesar antes y después del ejercicio, obteniendo la cantidad de líquido necesaria para hidratarnos correctamente.
</p>

<strong>¡CUATRO CONSEJOS PARA ESTAR MOTIVADO!</strong>
<p>

1. Establece metas realistas: fijar metas a corto, mediano y largo plazo te permitirá reencontrarte con la motivación cada vez que cumplas un objetivo o que te acerques a él. De ahí que las metas deban ser realistas y periódicas. Sin metas no se tendrá razón para estar en el gym.

2. Cuenta hacia atrás: en cada repetición, no empieces a contar desde cero, comienza desde el número de repeticiones que debes hacer y ve restando. Así visualizarás antes la meta.

3. Música: es uno de los elementos motivadores más importantes, aunque su efectividad varía de persona a persona. Hay quienes prefieren estar concentrados en su respiración y el entorno, otros no. El ritmo de la música puede ser fundamental para mantenerte activo. 

4. Entretención: Cuando comienzas a ver el gimnasio como una obligación, algo está mal. Puede que le estés dedicando mucho, puede que estés haciendo siempre lo mismo, puede que necesites cambiar el horario o que necesites ir con alguien, pero ahí la gracia: hacer cambios, probar rutinas en diferente orden, asistirse con un compañero de entrenamiento, cambiar la playlist de música, combinar la rutina del gym con otra disciplina, o cualquier otro aspecto que pueda ayudar a no perder la motivación del gimnasio.
</p>
